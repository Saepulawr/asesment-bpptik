<p>LSP BPPTIK adalah lembaga pendukung BNSP yang bertanggung jawab untuk melaksanakan uji kompetensi dan
    sertifikasi
    kompetensi di bidang TIK. LSP BPPTIK didirikan oleh Badan Penelitian dan Pengembangan Sumber Daya Manusia
    (Balitbang SDM) Kementerian Komunikasi dan Informatika (Kominfo) melalui Surat Keputusan (SK) Kepala
    Balitbang
    SDM Nomor 13 Tahun 2018 tanggal 1 Februari 2018 tentang Pembentukan Lembaga Sertifikasi Profesi Pihak Kesatu
    Balai Pelatihan dan Pengembangan Teknologi Informasi dan Komunikasi.</p>
<p>LSP BPPTIK memiliki fungsi melaksanakan sertifikasi kompetensi uji kompetensi, dan sertifikasi bidang TIK,
    dengan
    tugas: <br>
    a. menyusun dan mengembangkan skema sertifikasi<br>
    b. membuat perangkat asesmen dan uji kompetensi<br>
    c. menyediakan tenaga penguji (asesor) <br>
    d. melaksanakan uji kompetensi dan sertifikasi <br>
    e. melaksanakan surveilan pemeliharaan sertifikasi <br>
    f. menetapkan persyaratan, memverifikasi dan menetapkan Tempat Uji Kompetensi (TUK) <br>
    g. memelihara kinerja asesor dan TUK dan <br>
    h. mengembangkan pelayanan uji kompetensi dan sertifikasi.</p>
<p>Dalam melaksanakan fungsi dan tugasnya, LSP BPPTIK mengacu pada pedoman yang dikeluarkan oleh BNSP. Dalam
    pedoman tersebut ditetapkan persyaratan yang harus dipatuhi untuk menjamin agar lembaga sertifikasi
    menjalankan sistem sertifikasi secara konsisten dan profesional, sehingga dapat diterima pada tingkat
    nasional yang relevan demi kepentingan pengembangan sumber daya manusia dalam aspek peningkatan kualitas dan
    perlindungan tenaga kerja.</p>
